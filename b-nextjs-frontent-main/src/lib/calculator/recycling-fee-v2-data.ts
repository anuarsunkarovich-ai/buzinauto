// ─── Вспомогательные функции ──────────────────────────────────────────

/**
 * Множитель года относительно базы 2025.
 * 2025: 1.0, 2026: 1.2, 2027: 1.32, 2028: 1.452, 2029: 1.5972, 2030+: 1.75692
 */
function getYearMultiplier(year: number): number {
  if (year <= 2025) return 1
  if (year === 2026) return 1.2
  const capped = Math.min(year, 2030)
  return 1.2 * Math.pow(1.1, capped - 2026)
}

/**
 * Оценка мощности двигателя (кВт) по объёму (см³).
 * Используется когда точное значение кВт не указано.
 */
function estimatePowerKw(volumeCc: number): number {
  if (volumeCc <= 1000) return 55
  if (volumeCc <= 2000) return 110
  if (volumeCc <= 3000) return 155
  if (volumeCc <= 3500) return 220
  return 270
}

/** Найти скобку мощности по значению кВт */
function findBracket(brackets: readonly PowerBracket[], powerKw: number): PowerBracket {
  for (const b of brackets) {
    if (powerKw <= b[0]) return b
  }
  return brackets[brackets.length - 1]
}

/** Найти категорию объёма по значению см³ */
function findVolumeCategory(
  categories: readonly VolumeCategory[],
  volumeCc: number,
): VolumeCategory {
  for (const c of categories) {
    if (volumeCc <= c.maxVolume) return c
  }
  return categories[categories.length - 1]
}

// ─── Публичный API ────────────────────────────────────────────────────

/**
 * Получить итоговый коэффициент утилизационного сбора (с учётом индексации по году).
 *
 * @param importerType  — «individual» (физлицо) или «legal» (юрлицо)
 * @param engineType    — тип двигателя
 * @param engineVolumeCc — объём двигателя в см³ (0 для электромобилей)
 * @param isNew         — true если авто ≤3 лет
 * @param enginePowerKw — мощность двигателя в кВт (если не указана — оценивается по объёму)
 * @param year          — год расчёта (по умолчанию текущий)
 */
export function getRecyclingFeeCoefficient(params: {
  importerType: ImporterType
  engineType: EngineType
  engineVolumeCc: number
  isNew: boolean
  enginePowerKw?: number
  year?: number
}): number {
  const {
    importerType,
    engineType,
    engineVolumeCc,
    isNew,
    enginePowerKw,
    year = new Date().getFullYear(),
  } = params

  const table = importerType === 'individual' ? INDIVIDUAL_TABLE : LEGAL_TABLE
  const powerKw = enginePowerKw ?? estimatePowerKw(engineVolumeCc)
  const isElectric = engineType === 'electric'

  const brackets = isElectric
    ? table.electric
    : findVolumeCategory(table.ice, engineVolumeCc).brackets

  const bracket = findBracket(brackets, powerKw)
  const baseCoeff = isNew ? bracket[1] : bracket[2]

  // Льготный тариф — фиксированный, без индексации по годам
  if (baseCoeff === P) {
    return isNew ? PREFERENTIAL_NEW : PREFERENTIAL_USED
  }

  return baseCoeff * getYearMultiplier(year)
}
